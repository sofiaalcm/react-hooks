import * as React from 'react'

function Greeting({initialName = ''}) {
  // üê® initialize the state to the value from localStorage
  // üí∞ window.localStorage.getItem('name') || initialName // Um lugar do proprio navegador que proporciona que guarde variaves, mesmo que seja recarregado a pagina "Cookie associado a p√°gina que permite a recupera√ß√£o de informa√ß√£o"
  
  // Lazy INIITIALIZER: inicializador "pregui√ßoso"
  //Quando o useState recebe uma fun√ß√£o em vez de um valor como estado inicial, 
  // essa fun√ß√£o √© execultada apenas durante a fase do componente, sem se 
  // reptir na fase update.
  const [name, setName] = React.useState(() => window.localStorage.getItem('name') || initialName) // Ao inves de carregar o valor da variavel, s√≥ sera carregada o valor que tinha anteriormente. 
  const [count, setCount] = React.useState(0)
  const [nameUC, setNameUC] = React.useState(() => window.localStorage.getItem('nameUC') || initialName)

  // üê® Here's where you'll use `React.useEffect`.
  // The callback should set the `name` in localStorage.
  // üí∞ window.localStorage.setItem('name', name)

  function handleChange(event) {
    setName(event.target.value)
    //window.localStorage.setItem('name', name) // Atualizando o localStorage dentro do handleChange
  }

  function handleClick(event) {
      setNameUC(event.target.value.toUpperCase())
  }

  // Efeito colateral a ser executado ap√≥s o componente ter sido atualizado
  React.useEffect(() => {
      // O valor do localStorges ser√° atualizado ap√≥s a atualiza√ß√£o do componente 
      window.localStorage.setItem('name', name)
      window.localStorage.setItem('name', nameUC)
      setCount(count + 1)
  }, [name, nameUC]) //[] √© a lista de depend√™ncias, ou seja, esse useEffect √© para ser chamado 
  // apenas quando a vari√°vel name sofrer altera√ß√£o

  return (
    <div>
      <form>
        <label htmlFor="name">Name: </label>
        <input value={name} onChange={handleChange} onClick={handleClick} id="name" />
      </form>
  {name ? <strong>Hello {name}, {nameUC}</strong> : 'Please type your name'}
  <p>localStorage: {window.localStorage.getItem('name')} - - {window.localStorage.getItem('nameUC')}</p>
<p> Contagem: {count}</p> 
    </div>
  )
}

function App() {
  return <Greeting />
}

export default App
